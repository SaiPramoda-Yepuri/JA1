package ex_13_Functions;

public class Method_Datatypes {
    public static void main(String[] args) {
      voidfun();
      retur_int();
      return_boolean();
      return_float_pi_value();
      return_long();
      return_String();

    }
    static  void voidfun(){
        System.out.println("void function");
    }
    static int retur_int(){
        System.out.println("Int return");
        return 10;
    }
    static  boolean return_boolean(){
        return  true;
    }
    static  float return_float_pi_value(){
        return 3.14f;
    }
    static long return_long(){
        return 10l;
    }
    static String return_String(){
        return "Pramoda";
    }
}
