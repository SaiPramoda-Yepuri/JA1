package ex_03_Literals;

public class Literals {
    public static void main(String[] args) {
        char new_line = '\n';
        char tab_line = '\t';
        char back_space = '\b';
        char carriage_return = '\r';
        System.out.println("YS Pramoda");
        System.out.println("Pramoda"+new_line+"YS");
        System.out.println("Pramoda"+tab_line+"YS");
        System.out.println("Pramoda"+back_space+"YS");
        System.out.println("Pramoda"+carriage_return+"YS");
        System.out.println("Hi"+"second line");
        char c11 = '\u1f60';
        System.out.println(c11);


    }
}
