package ex_10_For_Loop;

public class For_Loop {
    public static void main(String[] args) {
        for (int i=0; i<10 ; i++){
            System.out.println(i);
        }
        for (int _1=0; _1<=10 ; _1++){
            System.out.println(_1);
        }
        for (int c=0; c>1; c++){
            System.out.println(c);
        }
        System.out.println("End");
    }
}
