package ex_10_For_Loop;

public class For_If_loop {
    public static void main(String[] args) {
        for (int John=0; John<18; John++){
            if(John>15){
                System.out.println(John+"years - Gift for birthday is iphone");
            }
            else {
                System.out.println(John+"years - Only birthday party with cakes and chocolates");
            }
        }
    }
}
