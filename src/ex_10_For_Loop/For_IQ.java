package ex_10_For_Loop;

public class For_IQ {
    public static void main(String[] args) {
        for (int i = 0; i>10;){
            System.out.println("Hello");
        }
        for(long i=1l; i<10; i++){
            System.out.println(i);
        }
        for(float f=0.0f; f< 10.67;f++)
        {
            System.out.println(f);
        }
    }
}
