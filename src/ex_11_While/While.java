package ex_11_While;

public class While {
    public static void main(String[] args) {
        int i = 0;
        while(i<10){
            System.out.println(i);
            i++;
        }
        int i1 = 0;
        while(i1>=1){
            System.out.println("Enter");
            System.out.println(i1);
            i1--;
        }
    }
}
